// A simple for loop example
console.log("Counting from 1 to 5:");

for (let i = 1; i <= 5; i++) {
  // Explanation:
  // 1. 'let i = 1': Initialization - the loop starts with i = 1.
  // 2. 'i <= 5': Condition - the loop will run as long as i is less than or equal to 5.
  // 3. 'i++': Increment - after each iteration, i is increased by 1.

  console.log(`Current value of i: ${i}`); // Prints the current value of i
}
// When i becomes greater than 5, the loop stops.
