let a = true;
let b = false;

// AND (&&): Returns true if both operands are true; otherwise, returns false.
console.log(`a && b: ${a && b}`); // Output: false (true AND false = false)

// OR (||): Returns true if at least one operand is true; otherwise, returns false.
console.log(`a || b: ${a || b}`); // Output: true (true OR false = true)

// NOT (!): Returns the opposite of the operand (negation).
console.log(`!a: ${!a}`); // Output: false (NOT true = false)
console.log(`!b: ${!b}`); // Output: true (NOT false = true)
