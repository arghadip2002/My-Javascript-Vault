// Assign values to x and y
let x = 10;
let y = 5;

// Perform operations and store results
let addition = x + y; // Addition
let subtraction = x - y; // Subtraction
let multiplication = x * y; // Multiplication
let division = x / y; // Division
let modulus = x % y; // Remainder
let power = Math.pow(x, y); // Exponentiation (x^y)
let squareRoot = Math.sqrt(x); // Square root of x
let cubeRoot = Math.cbrt(x); // Cube root of x
let absolute = Math.abs(-x); // Absolute value of x
let round = Math.round(4.6); // Round
let floor = Math.floor(4.9); // Floor
let ceil = Math.ceil(4.1); // Ceiling
let random = Math.random(); // Random number between 0 and 1
let min = Math.min(x, y, 20); // Minimum of x, y, and 20
let max = Math.max(x, y, 20); // Maximum of x, y, and 20

// Trigonometric functions (using x as an angle in radians)
let sin = Math.sin(x);
let cos = Math.cos(x);
let tan = Math.tan(x);

// Logarithmic and exponential functions
let naturalLog = Math.log(x); // Natural log of x
let base10Log = Math.log10(x); // Base-10 log of x
let exp = Math.exp(x); // e^x

// Output all results
console.log(`Addition: ${addition}`);
console.log(`Subtraction: ${subtraction}`);
console.log(`Multiplication: ${multiplication}`);
console.log(`Division: ${division}`);
console.log(`Modulus: ${modulus}`);
console.log(`Power (x^y): ${power}`);
console.log(`Square Root of x: ${squareRoot}`);
console.log(`Cube Root of x: ${cubeRoot}`);
console.log(`Absolute value of x: ${absolute}`);
console.log(`Rounded value: ${round}`);
console.log(`Floor value: ${floor}`);
console.log(`Ceiling value: ${ceil}`);
console.log(`Random number: ${random}`);
console.log(`Minimum value: ${min}`);
console.log(`Maximum value: ${max}`);
console.log(`Sine of x: ${sin}`);
console.log(`Cosine of x: ${cos}`);
console.log(`Tangent of x: ${tan}`);
console.log(`Natural log of x: ${naturalLog}`);
console.log(`Base-10 log of x: ${base10Log}`);
console.log(`Exponential of x (e^x): ${exp}`);
